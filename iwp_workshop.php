<h2>The Painting Workshop</h2>
<div>
<p class="para">Welcome to The Painting Workshop!</p>
<p class="para">We have been offering fun, lively, quality art classes and workshops since 1995.</p>
<p class="para">We believe, as Picasso says, that "everyone is an artist". </p>
<p class="para1">Young or old, the art of creating is a joyous, exciting process, and we try to provide the room, space, and materials for that to happen. We encourage students to try new things, to be inspired by other artists, to find their voice</p>

